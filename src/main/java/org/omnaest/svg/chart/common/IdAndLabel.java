package org.omnaest.svg.chart.common;

public class IdAndLabel
{
	private String	id;
	private String	label;

	public IdAndLabel(String id, String label)
	{
		super();
		this.id = id;
		this.label = label;
	}

	public String getId()
	{
		return id;
	}

	public String getLabel()
	{
		return label;
	}

}